                    "teaches orcs, if not discipline, at least more efficient ways to use their strength. Varg favors weapons that not only deal damage, but also instill fear. The Iron " +
                    "Warrior knows that intimidation can defeat an enemy as quickly as any wound, and he sees no shame in striking from behind at a foe in retreat. Varg is depicted as " +
                    "an orc general wearing half-plate armor that was assembled from varied sources. He never wears a helm that would hinder his ability to see and hear what is happening " +
                    "on the battlefield. Varg has honed his abilities through countless battles against his brothers and the gods of other pantheons. When not opposing the Iron Warrior, " +
                    "the other orc gods depend on him to equip their armies. Varg is the most inventive and forward thinking of orc gods, but his genius is only for battle. His followers " +
                    "have little interest in using technology to improve other aspects of orc life, and the most devout of his faithful see innovation in other arenas as a blasphemous " +
                    "misuse of Varg’s gifts. All but the most savage orcs make use of metal weapons and armor, and they give thanks to Varg when they craft or steal these items. Tribes " +
                    "dedicated to the Iron Warrior make extensive use of siege weapons, and individual worshipers compete against each other to build the most complex and destructive " +
                    "devices of death.");
