                bp.SetDescription("\nTitles: Bark Breaker " +
                "\nAlignment: Neutral Evil   " +
                "\nAreas of Concern: Arson, Hunt, Ritual " +
                "\nDomains: Animal, Evil, Fire, Travel " +
                "\nSubdomains: Arson, Daemon, Fur, Smoke " +
                "\nFavoured Weapon: Dogslicer (Kukri)   " +
                "\nHoly Symbol: Severed dog's paw " +
                "\nSacred Animal: Goblin dog " +
