                    DeityTools.DomainAllowed.StarsDomainAllowed,
                    DeityTools.SeparatistDomainAllowed.AshDomainAllowedSeparatist,
                    DeityTools.SeparatistDomainAllowed.DivineDomainAllowedSeparatist,
