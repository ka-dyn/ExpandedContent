                    "\nSubdomains: Azata, Fate, Light, Whimsy   " +
                    "\nFavoured Weapon: Sling (Slingstaff)   " +
                    "\nHoly Symbol: Rainbow with shadow   " +
                    "\nSacred Animal: Lacewing   " +
