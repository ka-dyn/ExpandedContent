                    "that everyone's inner nature should be a similar mix of action and restraint, placid adoration and righteous scorn. He offers " +
                    "rest and relaxation to those who meet each day with excitement and vigor and who may be tired from the chaotic beauty that is " +
                    "their life. To mortal eyes, Lalaci appears to be fair-skinned with white-blonde hair and yellow-white eyes. He tucks the two prismatic " +
                    "wings that curve from his shoulders down so that he appears to wear a beautiful multicolored cloak, though among his own kind he walks " +
                    "in his true naked form, with crystalline flesh that refracts light as golden rays. He of Motley Repose provides rest to the " +
                    "hardworking and shade to those exhausted from the sun. Glassblowers and illusionists often find creative inspiration in Lalaci's " +
                    "rainbows; particularly gifted glass artists and lightweavers who lead worthy lives sometimes find small prisms at their workstations-tokens " +
                    "of Lalaci's favor. His devotees claim that the reflections of rainbows in natural bodies of water create portals to his celestial domain: " +
                    "an enormous, warped glass palace that floats on a cloud across the surface of Elysium.");
                bp.m_Icon = LalaciIcon;
                bp.Ranks = 1;
                bp.IsClassFeature = true;
                bp.HideInCharacterSheetAndLevelUp = false;
                bp.SetDisallowedArchetype(ClericClass, PriestOfBalance);
                bp.SetDisallowedArchetype(WarpriestClass, FeralChampionArchetype);
                bp.SetDisallowedArchetype(InquistorClass, SwornOfTheEldestArchetype);
                bp.DisallowDarkSister();
                bp.DisallowProphetOfPestilence();
                bp.MagicDeceiverLock();
                bp.DisallowNewMantisZealot();

                bp.Groups = new FeatureGroup[] { FeatureGroup.Deities };
                bp.AddComponent<PrerequisiteAlignment>(c => {
                    c.Alignment = AlignmentMaskType.NeutralGood | AlignmentMaskType.ChaoticGood | AlignmentMaskType.ChaoticNeutral;
                });
                bp.AddComponent<AddFacts>(c => {
                    c.m_Facts = new BlueprintUnitFactReference[] {
                        ChannelPositiveAllowed.ToReference<BlueprintUnitFactReference>()
                    };
                });
                bp.SetAllowedDomains(
                    DeityTools.DomainAllowed.ChaosDomainAllowed,
                    DeityTools.DomainAllowed.GoodDomainAllowed,
                    DeityTools.DomainAllowed.LuckDomainAllowed,
