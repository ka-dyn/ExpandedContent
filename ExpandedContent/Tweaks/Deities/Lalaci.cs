            var LalaciFeature = Helpers.CreateBlueprint<BlueprintFeature>("LalaciFeature", (bp => {
                bp.SetName("Lalaci");
                bp.SetDescription("\nTitles: He of Motley Repose   " +
                    "\nAlignment: Chaotic Good   " +
