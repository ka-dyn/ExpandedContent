                    "not fit her needs and desires. Regardless of the reason, all know that Reymenda now swears an oath to never have children and expects the same " +
                    "of her followers. In exchange for this vow, Reymenda grants her followers the guidance and might they need to bear the responsibility of virtue. " +
                    "While some set out to strike down evil, others rally a community in the wake of a natural disaster, or do something as simple as helping a family " +
                    "by working in their kitchen for an evening when the family is too overburdened by life to cook a meal. No genuinely helpful responsibility is too " +
                    "small for a follower of Reymenda, though they know how to teach others to help themselves or work together, and they also do their best to avoid " +
                    "exploitation when someone grows careless and irresponsible. " +
                    "\nIn addition to responsibilities, Reymenda focuses on endings. Life is full of moments of finality; Reymenda’s followers serve both as instigators " +
                    "of endings and counsellors when endings occur. When facing evil, Reymenda teaches to be decisive, stopping evil as soon as one can. When dealing " +
                    "with other endings, such as the death of another or the end of a relationship, Reymenda teaches acceptance. \nAlthough Reymenda demands her " +
                    "followers remain childless, she does not abhor children. In fact, many of her teachings include the protection of children. Reymenda asks " +
                    "her followers to undertake dangerous missions and a lack of children makes these tasks easier. It’s not uncommon for her followers to eventually " +
                    "choose to have children. Reymenda encourages this, but no longer grants her blessings to these followers, instead asking them to find a different deity. " +
                    "Reymenda of all gods is used to these endings.");
                bp.m_Icon = ReymendaIcon;
                bp.Ranks = 1;
                bp.IsClassFeature = true;
                bp.HideInCharacterSheetAndLevelUp = false;
                bp.SetDisallowedArchetype(ClericClass, PriestOfBalance);
                bp.SetDisallowedArchetype(WarpriestClass, FeralChampionArchetype);
                bp.SetDisallowedArchetype(InquistorClass, SwornOfTheEldestArchetype);
                bp.DisallowDarkSister();
                bp.DisallowProphetOfPestilence();
                bp.MagicDeceiverLock();
                bp.DisallowNewMantisZealot();
                bp.DisallowSoldierOfGaia();

                bp.Groups = new FeatureGroup[] { FeatureGroup.Deities };
                bp.AddComponent<PrerequisiteAlignment>(c => {
                    c.Alignment = AlignmentMaskType.NeutralGood | AlignmentMaskType.ChaoticGood | AlignmentMaskType.ChaoticNeutral;
                });
                bp.AddComponent<AddFacts>(c => {
                    c.m_Facts = new BlueprintUnitFactReference[] {
                        ChannelPositiveAllowed.ToReference<BlueprintUnitFactReference>()
                    };
                });
                bp.SetAllowedDomains(
                    DeityTools.DomainAllowed.ChaosDomainAllowed,
                    DeityTools.DomainAllowed.GoodDomainAllowed,
                    DeityTools.DomainAllowed.DestructionDomainAllowed,
                    DeityTools.DomainAllowed.HealingDomainAllowed,
                    DeityTools.DomainAllowed.RestorationDomainAllowed,
                    DeityTools.SeparatistDomainAllowed.AshDomainAllowedSeparatist,
