                bp.SetDescription("\nTitles: Master of the Final Incantation, Demon Lord of Forbidden Lore and Magic  " +
                    "\nAlignment: Chaotic Evil   " +
                    "\nAreas of Concern: Forbidden lore, Magic, Snakes  " +
                    "\nDomains: Chaos, Evil, Knowledge, Magic  " +
                    "\nSubdomains: Arcane, Demon, Education, Memory, Thought   " +
                    "\nFavoured Weapon: Flail   " +
                    "\nHoly Symbol: Demonic face encircled by a serpent with two snake tails descending from the mouth   " +
                    "\nSacred Animal: Snake   " +
                    "\nAbraxas is the demon lord of forbidden lore and magic, and called the Master of the Final Incantation. He is said to know countless magical " +
                    "formulae, spells, and secrets, including the Final Incantation, a single word so powerful its speaking causes the annihilation of magic." +
                    "Although Abraxas’s following is strongest among the drow of Golarion, one can expect to find small cults of Abraxas in most large cities on the surface. " +
                    "His cult is particularly strong in Nex’s capital city of Quantium, where it maintains a notorious library called Scrivenbough, a fortified stone structure " +
                    "with countless rare books in its holdings, and cultists who tattoo their greatest secrets on hidden parts of their bodies.");
                bp.m_Icon = AbraxasIcon;
                bp.Ranks = 1;
                bp.IsClassFeature = true;
                bp.HideInCharacterSheetAndLevelUp = false;
                bp.SetDisallowedArchetype(ClericClass, PriestOfBalance);
                bp.SetDisallowedArchetype(WarpriestClass, FeralChampionArchetype);
                bp.SetDisallowedArchetype(DreadKnightClass, ClawOfTheFalseWyrmArchetype);
                bp.SetDisallowedArchetype(InquistorClass, SwornOfTheEldestArchetype);
                bp.DisallowAngelfireApostle();
                bp.DisallowDarkSister();
                bp.MagicDeceiverLock();
                bp.DisallowNewMantisZealot();

                bp.Groups = new FeatureGroup[] { FeatureGroup.Deities };
                bp.AddComponent<PrerequisiteAlignment>(c => {
                    c.Alignment = AlignmentMaskType.ChaoticNeutral | AlignmentMaskType.NeutralEvil | AlignmentMaskType.ChaoticEvil;
                });
                bp.AddComponent<AddFacts>(c => {
                    c.m_Facts = new BlueprintUnitFactReference[] { 
                        ChannelNegativeAllowed.ToReference<BlueprintUnitFactReference>() 
                    };
                });
                bp.SetAllowedDomains(
                    DeityTools.DomainAllowed.ChaosDomainAllowed,
                    DeityTools.DomainAllowed.EvilDomainAllowed,
                    DeityTools.DomainAllowed.KnowledgeDomainAllowed,
                    DeityTools.DomainAllowed.MagicDomainAllowed,
                    DeityTools.DomainAllowed.ArcaneDomainAllowed,
                    DeityTools.DomainAllowed.DemonDomainChaosAllowed,
