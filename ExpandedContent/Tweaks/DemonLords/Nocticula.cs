                bp.SetName("Nocticula");
                bp.SetDescription("\nTitles: Our Lady in Shadow, Demon Lord of Darkness and Lust   " +
                    "\nAlignment: Chaotic Evil   " +
                    "\nAreas of Concern: Assassins, Darkness, Lust   " +
                    "\nDomains: Chaos, Charm, Darkness, Evil   " +
