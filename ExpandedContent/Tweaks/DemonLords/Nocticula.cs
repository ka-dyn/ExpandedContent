                    "literally and figuratively). Assassins' guilds in her service are established in many cities and are constantly at odds with other " +
                    "guilds, especially the feared Red Mantis, often leading to protracted shadow wars. She is particularly popular amongst the drow of " +
                    "Golarion, and is the patron of House Misraria. She is also worshiped in places of decadence such as Katapesh, Nex, Geb, and certain " +
                    "River Kingdoms. Nocticula is also venerated among the Lamyros, a group of Pharasma-cursed creatures that loathe all the gods, and take " +
                    "pleasure in confounding and destroying their mortal servants. Nocticula appears as a succubus, wearing her dark hair in a complex style. " +
                    "Her eyes devoid of pupils, her fingers tipped with talons, and her feet end in stony hooves that exude red-hot, liquid iron.");
                bp.m_Icon = NocticulaIcon;
                bp.Ranks = 1;
                bp.IsClassFeature = true;
                bp.HideInCharacterSheetAndLevelUp = false;
                bp.SetDisallowedArchetype(ClericClass, PriestOfBalance);
                bp.SetDisallowedArchetype(WarpriestClass, FeralChampionArchetype);
                bp.SetDisallowedArchetype(DreadKnightClass, ClawOfTheFalseWyrmArchetype);
                bp.SetDisallowedArchetype(InquistorClass, SwornOfTheEldestArchetype);
                bp.DisallowAngelfireApostle();
                bp.DisallowDarkSister();
                bp.MagicDeceiverLock();
                bp.DisallowNewMantisZealot();

                bp.Groups = new FeatureGroup[] { FeatureGroup.Deities };
                bp.AddComponent<PrerequisiteAlignment>(c => {
                    c.Alignment = AlignmentMaskType.ChaoticNeutral | AlignmentMaskType.NeutralEvil | AlignmentMaskType.ChaoticEvil;
                });
                bp.AddComponent<AddFacts>(c => {
                    c.m_Facts = new BlueprintUnitFactReference[] { 
                        ChannelNegativeAllowed.ToReference<BlueprintUnitFactReference>() 
                    };
                });
                bp.SetAllowedDomains(
                    DeityTools.DomainAllowed.ChaosDomainAllowed,
                    DeityTools.DomainAllowed.DarknessDomainAllowed,
